#ifndef __ITERATOR_H
#define __ITERATOR_H

//=============================================================
// Класс, представляющий абстрактный интерфейс внешнего итератора
//=============================================================

template <class T>
class Iterator {
public :
  // Проверка окончания итерации
  virtual bool hasMoreElements() const = 0;

  // "Сдвиг" итератора на следующий элемент
  virtual Iterator<T> & operator ++() = 0;

  // Доступ к итерируемому элементу
  virtual T & operator *() = 0;
};

#endif
